    public void Data(){
        
        try {            
            Statement leer = conect.createStatement();
            ResultSet resultado = leer.executeQuery("call DataTrabajadores();");
            int i=0;
            int j=0;
            while (resultado.next()) {
                //jTable1.addRowSelectionInterval(i, i);
                jTable1.setValueAt(resultado.getString(1), j, 0);
                jTable1.setValueAt(resultado.getString(2), j, 1);
                jTable1.setValueAt(resultado.getString(3), j, 2);
                jTable1.setValueAt(resultado.getString(4), j, 3);
                jTable1.setValueAt(resultado.getString(5), j, 4);
                //jTable1.setValueAt(resultado.getString(6), j, 5);
                j++;
            }
            
            
        } catch (Exception e) {
        }
        
        
        
    }
    

    
    
    
    
    
